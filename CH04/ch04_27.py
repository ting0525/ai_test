import tensorflow as tf

a = tf.constant([[1,2,3,4],[4,5,6,7],[7,8,9,10]])
print("a[::] = ",a[::])   # 讀取所有元素
print("a[:] = ",a[:])     # 讀取所有元素, 與 a[::]同
print("a[0] =",a[0])      # 讀取a[0]的所有元素
print("a[1:2,::] = ",a[1:2,::])    # 讀取 a[1] 的所有元素
print("a[:,0:4:2] = ", a[:,0:4:2])  # 讀取所有列中第0~3行,行步階為2所有元素